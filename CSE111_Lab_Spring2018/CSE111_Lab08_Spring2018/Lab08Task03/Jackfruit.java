public class Jackfruit extends Fruit{
    
    public boolean formalin = true;
    public String name = "Jackfruit";
    
    public Jackfruit(){
        super(true, "Jackfruit");
    }
    public String getName(){
        return name;
    }
    
    public boolean hasFormalin(){
        return formalin;
    }
    
}

    